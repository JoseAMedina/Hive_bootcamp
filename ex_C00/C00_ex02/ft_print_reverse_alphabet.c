#include <unistd.h>

void	ft_print_reverse_alphabet(void)
{
	char	a; // variable

	a = 'z';
	write(1, "\n", 1);
	write(1, "Invers alphabet:\n", 18);
	while (a >= 'a')
	{	
		write(1, &a, 1);
		a--;
	}
}
